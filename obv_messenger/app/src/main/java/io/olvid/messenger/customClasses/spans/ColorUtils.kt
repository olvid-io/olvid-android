package io.olvid.messenger.customClasses.spans

import androidx.annotation.ColorInt
import androidx.annotation.IntRange

object ColorUtils {
    @ColorInt
    fun applyAlpha(
        @ColorInt color: Int,
        @IntRange(from = 0, to = 255) alpha: Int
    ): Int {
        return color and 0x00FFFFFF or (alpha shl 24)
    }
}